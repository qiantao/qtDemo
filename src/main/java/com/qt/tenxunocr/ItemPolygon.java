public class ItemPolygon {
    private Long X;
    private Long Y;

    private Long Width;
    private Long Height;

    public Long getX() {
        return X;
    }

    public void setX(Long x) {
        X = x;
    }

    public Long getY() {
        return Y;
    }

    public void setY(Long y) {
        Y = y;
    }

    public Long getWidth() {
        return Width;
    }

    public void setWidth(Long width) {
        Width = width;
    }

    public Long getHeight() {
        return Height;
    }

    public void setHeight(Long height) {
        Height = height;
    }
}
